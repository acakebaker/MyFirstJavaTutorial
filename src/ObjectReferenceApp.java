public class ObjectReferenceApp {
    public static void main(String[] args) {
        Byte numberOfSeats = 5;
        Short horsePower = 392;
        Integer price = 14999;
        Long registrationNumber = 34507897358543859L;
        Float fuelConsumptionCombined = 15.5F;
        Double fuelConsumptionPrecise = 15.4478383475835763892074D;
        Boolean isDamaged = true;
        Character energyEfficiencyCategory = 'G';
        String carModel = "Dodge Challenger SRT 392";
        String carModel2 = new String("Dodge Challenger SRT 392");

        System.out.println(numberOfSeats);
        System.out.println(horsePower);
        System.out.println(price);
        System.out.println(registrationNumber);
        System.out.println(fuelConsumptionCombined);
        System.out.println(fuelConsumptionPrecise);
        System.out.println(isDamaged);
        System.out.println(energyEfficiencyCategory);
        System.out.println(carModel);
        System.out.println(carModel2);

    }
}
